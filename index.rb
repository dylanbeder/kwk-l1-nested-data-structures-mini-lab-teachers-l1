def sandwich_request(ingredients, order)
  i=0
  n=false
  order.each do |item|
    while i<ingredients.length
      if ingredients[i].include?(item)==true
        n==true
        break
      else
        i=i+1
      end
    end
    if n==false
      return false
    end
  end
  return true
end

